using UnityEngine;

public class FinishZone : MonoBehaviour
{
    private bool activated = false;

    void OnTriggerEnter(Collider other)
    {
        // ????????? ???????? PlayerController.puzzleSolved
        if (other.CompareTag("Player") && !activated && PlayerController.puzzleSolved)
        {
            activated = true;
            PlayerController pc = other.GetComponent<PlayerController>();

            if (pc != null)
            {
                StartCoroutine(pc.FinalCutscene());
            }
        }
        else if (other.CompareTag("Player") && !PlayerController.puzzleSolved)
        {
            // ????? ??????? ? ??????? ??? ?????, ??? ????? ??? ?? ???????
            Debug.Log("????! ????? ??? ?? ???????.");
        }
    }
}