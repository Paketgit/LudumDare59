using System;
using UnityEngine;
using UnityEngine.SceneManagement;

public class GameManager : MonoBehaviour
{
    [SerializeField] private int CountOfTasks = 4; // ??????? 4 ? ??????????
    private int CoundOfTasksEnded = 0;

    public static Action TaskEnded;
    public static Action TaskIncomplite;

    void Start()
    {
        G.currentGameManager = this;

        // ????????????? ?? ???????
        TaskEnded += TaskCheck;
        TaskIncomplite += TaskIncomplitet;
    }

    private void TaskIncomplitet()
    {
        CoundOfTasksEnded--;
        Debug.Log("???? ????? ????????. ?????? ?????????: " + CoundOfTasksEnded);
    }

    public void TaskCheck()
    {
        CoundOfTasksEnded++;
        Debug.Log("????? ?????????! ?????: " + CoundOfTasksEnded + " / " + CountOfTasks);

        if (CoundOfTasksEnded >= CountOfTasks)
        {
            // ????????????? ???? ??? 3D ????? (????? ????????? ?????)
            PlayerController.puzzleSolved = true;

            // ????????? MainScene
            SceneManager.LoadScene("MainScene");
        }
    }

    private void OnDestroy()
    {
        // ??????????? ????????????, ????? ??? ????????????? ??????? ?? ???????
        TaskEnded -= TaskCheck;
        TaskIncomplite -= TaskIncomplitet;
    }
}